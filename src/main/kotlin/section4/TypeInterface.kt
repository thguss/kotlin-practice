package section4

fun main() {

    /**
     * 대부분은 코틀린의 타입 추론 기능을 이용하여
     * 코드량을 줄일 수 있다.
     */

    var a = 1234 // Int
    var b = 1234L // Long

    var c = 123.4 // Double
    var d = 123.4f // Float

    var e = 0xABCD // Int (16진수)
    var f = 0b010101 // Int (2진수)

    var g = true // Boolean
    var h = 'c' // Char
}